    <h2 class="title">&nbsp;Information Architecture</h2>
    <p>
      <a href="https://www.usability.gov/what-and-why/information-architecture.html" target="_blank">Information architecture</a> is the way information is organized, labeled and interconnected within a website. References are made in how links make a navigation system, how sections are named and how categories are assigned. Good IA makes locating and understanding collections of information easy and this is done through proper naming and linking.
    </p>
    <p>
      A major focus of IA is the ability to adapt or scale a website in anticipation of environmental changes. Use of web analytics, user feedback and research to evaluate change is a required skill. Scheduling reminders to monitor data sources and automating processes to run random site tests are tactics that will make the process less frustrating.
    </p>
    <!-- IA Tenants -->
    <article>
      <h3 class="title">&nbsp;Main Tenants of IA</h3>
      <p>
        The Morville/Rosenfield IA approach defines information as an ecological relationship between content, users and context. At the intersection of a venn-diagram, devs must understand purpose of content, what users care about and how it helps complete a goal. Thus, devs should adhere to four primary components. 
      </p>
      <p>
        <i>Organization Structures</i> divide content into distinct groups, establishing a hierarchal order amongst a collection of content. This is especially important when setting up navigation options and determining its scope as the goal is to organize information intuitively. For example, breadcrumbs or links can be used to show the current page in a website or move to another area of the site. 
      </p>
      <p>
        <i>Labeling Systems</i> group information to provide the context users need to understand how your website is organized. It is imperative to use descriptive words that users relate to, check for overlapping categories and use classifications that communicate attributes users can decipher.
      </p>
      <p>
        <i>Navigation Systems</i> include menus and links that help users navigate relevant inforation. Navigation should be used to define a scope that devs want the users to follow and connect with then add a ‘filter’ or search options (autocomplete) for users to shift through popular content.
      </p>
      <p>
        <i>Search Systems</i> allow users to enter a variety of criteria to discover relevant content, which is useful for users to search for something specific quickly when a website has complex information, many attributes and loads of content. Filter attributes provide users with options to apply a general or precise search of content.
      </p>
    </article>
    <!-- -->
    <h3 class="title">&nbsp;Evaluating IA</h3>
    <p>
      Such interactivity requires evaluating website analytics in order to understand users and how they utilize information. Analytics refers to popular content, information paths, link referrals and search terms, all of which can help organize information Low traffic to categories, low conversions, high bounce rates, low entrance rates and a high value of search properties help explain these analytics further. Sources of user data include analytics, search data, social media and surveys.
    </p>
    <!-- -->
    <h3 class="title">&nbsp;Applying IA</h3>
    <p>
       It is important to test usability to ensure everything works as intended, or at minimum that users do not become frustrated. A general <i>Usability Test</i> will have users speak out loud while completing common tasks while the dev quietly observes. The results should indicate how the average user makes decisions and interpret how the user accessess the information and interface. The key is to pinpoint user engagement using metrics that will help qualify modifications that should be made.
    </p>
    <p>
      There are various means by which devs can test IA, a good starting point of which would be to create then review a <i><a href="https://www.gloomaps.com/" target="_blank">sitemap</a></i> of the websites. A sitemap is a visual representation of website pages and sections with links that illustrate their connection. Ascertain the quality of IA organization by performing a <i>Tree Test</i>: a text-based sitemap given to a user who is asked to follow links to select the spot where they think they would find relevant information. <i><a href="https://www.optimalworkshop.com/101/card-sorting" target="_blank">Card sorting</a></i> is another testing method that devs use to understand user perspectives related to how the user would organize the information and name categories. After testing users, it is helpful to provide a <a href="https://www.mockplus.com/blog/post/user-experience-survey-questions" target="_blank">survey</a> to receive feedback.
    </p>
