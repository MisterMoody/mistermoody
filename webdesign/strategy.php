    <article>
      <h3>&nbsp;Color</h3>
      <p>
        Website color selection should align with brand values. Knowing ones demographic and how they respond to colors is imperative for cultivating a following. 
      </p>
      <p><span>Red</span> is a bold color that evokes a strong emotion. With its intensity, it creates a sense of urgency.</p>
      <p><span>Orange</span> is a cheerful and confident color that conveys the idea of enthusiasm. However, it can come off as the color of caution as well.</p>
      <p><span>Yellow</span> is a color that provides a cheerful feeling. It represents optimism and is usually attention grabbing. One thing to consider, however, is that some shades can strain the eye.</p>
      <p><span>Green</span> represents growth and nature. It signifies health, serenity, and tranquility. It is associated with wealth.</p>
      <p><span>Blue</span> is associated with water, and provides a feeling of calmness and serenity. Blue creates a sense of security and trust, and is often used for corporations.</p>
      <p><span>Purple</span> is a color of wealth and success. It’s a powerful color, but also represents creativity.</p>
      <p><span>Brown</span> is a friendly and earthy color, commonly associated with the outdoors.</p>
      <p><span>Black</span> is a color with a sophisticated feeling. It’s often what we think of with “sleek” brands because of its exclusivity and mystery.</p>
      <p><span>Grey</span> provides a feeling of security, reliability, and intelligence.</p>
      <p><span>White</span> provides a clean or neutral feeling. It’s a key color because it adds breathing room and what is referred to as white space.</p>
      <p>
        Use a color scheme of at least three complimenatary colors to distinguish element importance and neutral colors for text. Create harmony through coordinated accent colors. <a href="http://paletton.com/" target="_blank">Paletton</a> is a great tool for generating a good scheme.
      </p>
